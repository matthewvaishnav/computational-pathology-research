@dataclass
class StreamingReportData:
    """Data for streaming WSI clinical report."""

    # Patient/Slide info
    slide_id: str
    patient_id: Optional[str] = None
    scan_date: Optional[datetime] = None

    # Processing results
    prediction_class: str = ""
    confidence: float = 0.0
    processing_time: float = 0.0
    patches_processed: int = 0
    total_patches: int = 0

    # Attention data
    attention_heatmap_path: Optional[Path] = None
    confidence_plot_path: Optional[Path] = None

    # Quality metrics
    coverage_percent: float = 0.0
    throughput: float = 0.0
    memory_usage_gb: float = 0.0

    # Uncertainty quantification
    uncertainty_score: Optional[float] = None
    confidence_interval: Optional[tuple[float, float]] = None

    # Metadata
    model_version: str = "1.0.0"
    processing_mode: str = "streaming"
    report_timestamp: datetime = field(default_factory=datetime.now)

    # Clinical notes
    physician_notes: Optional[str] = None
    recommendations: List[str] = field(default_factory=list)


class StreamingReportGenerator:
    """Generate clinical reports for real-time WSI streaming results.

    Features:
    - PDF report templates with visualizations
    - Confidence metrics and uncertainty quantification
    - Processing metadata and quality metrics
    - Institutional branding support
    - Customizable report templates
    - Multi-language support (i18n)
    """

    def __init__(
        self,
        institution_name: str = "HistoCore Medical AI",
        institution_logo: Optional[Path] = None,
        template_config: Optional[ReportTemplate] = None,
        language: str = "en",
        custom_translations: Optional[Dict[str, Dict[str, str]]] = None,
    ):
        """Initialize report generator.

        Args:
            institution_name: Name of institution for branding
            institution_logo: Path to institution logo image
            template_config: Custom template configuration
            language: Language code ('en', 'es', 'fr', 'de')
            custom_translations: Custom translation dictionaries
        """
        if not REPORTLAB_AVAILABLE:
            raise ImportError(
                "reportlab required for PDF generation. Install with: pip install reportlab"
            )

        self.institution_name = institution_name
        self.institution_logo = institution_logo
        self.template = template_config or ReportTemplate()
        self.language = language

        # Setup translations
        self.translations = DEFAULT_TRANSLATIONS.copy()
        if custom_translations:
            self.translations.update(custom_translations)

        if language not in self.translations:
            logger.warning(f"Language '{language}' not supported, falling back to English")
            self.language = "en"

        # Setup styles
        self.styles = getSampleStyleSheet()
        self._setup_custom_styles()

        logger.info(
            f"Initialized StreamingReportGenerator for {institution_name} (language: {self.language})"
        )

    def t(self, key: str) -> str:
        """Translate a key to the current language.

        Args:
            key: Translation key

        Returns:
            Translated string
        """
        return self.translations[self.language].get(key, key)

    def _setup_custom_styles(self):
        """Setup custom paragraph styles for reports."""
        # Title style
        self.styles.add(
            ParagraphStyle(
                name="CustomTitle",
                parent=self.styles["Heading1"],
                fontSize=self.template.title_font_size,
                textColor=colors.HexColor("#1a1a1a"),
                spaceAfter=30,
                alignment=TA_CENTER,
                fontName="Helvetica-Bold",
            )
        )

        # Section header style
        self.styles.add(
            ParagraphStyle(
                name="SectionHeader",
                parent=self.styles["Heading2"],
                fontSize=self.template.section_font_size,
                textColor=colors.HexColor(self.template.primary_color),
                spaceBefore=20,
                spaceAfter=12,
                fontName="Helvetica-Bold",
            )
        )

        # Metric style
        self.styles.add(
            ParagraphStyle(
                name="Metric",
                parent=self.styles["Normal"],
                fontSize=self.template.body_font_size,
                textColor=colors.HexColor("#333333"),
                spaceAfter=6,
            )
        )

        # Confidence high style
        self.styles.add(
            ParagraphStyle(
                name="ConfidenceHigh",
                parent=self.styles["Normal"],
                fontSize=18,
                textColor=colors.HexColor(self.template.confidence_high_color),
                fontName="Helvetica-Bold",
            )
        )

        # Confidence medium style
        self.styles.add(
            ParagraphStyle(
                name="ConfidenceMedium",
                parent=self.styles["Normal"],
                fontSize=18,
                textColor=colors.HexColor(self.template.confidence_medium_color),
                fontName="Helvetica-Bold",
            )
        )

        # Confidence low style
        self.styles.add(
            ParagraphStyle(
                name="ConfidenceLow",
                parent=self.styles["Normal"],
                fontSize=18,
                textColor=colors.HexColor(self.template.confidence_low_color),
                fontName="Helvetica-Bold",
            )
        )

    def generate_pdf_report(self, report_data: StreamingReportData, output_path: Path) -> Path:
        """Generate PDF clinical report.

        Args:
            report_data: Report data with results and visualizations
            output_path: Path to save PDF report

        Returns:
            Path to generated PDF file
        """
        output_path = Path(output_path)
        output_path.parent.mkdir(parents=True, exist_ok=True)

        # Determine page size
        page_size = A4 if self.template.page_size == "a4" else letter
        margins = self.template.margins

        # Create PDF document
        doc = SimpleDocTemplate(
            str(output_path),
            pagesize=page_size,
            rightMargin=margins["right"] * inch,
            leftMargin=margins["left"] * inch,
            topMargin=margins["top"] * inch,
            bottomMargin=margins["bottom"] * inch,
        )

        # Build report content
        story = []

        # Header
        story.extend(self._build_header(report_data))

        # Patient/Slide information
        if self.template.include_patient_info:
            story.extend(self._build_patient_info(report_data))

        # Primary results
        story.extend(self._build_results_section(report_data))

        # Visualizations
        if self.template.include_visualizations:
            story.extend(self._build_visualizations_section(report_data))

        # Quality metrics
        if self.template.include_quality_metrics:
            story.extend(self._build_quality_metrics(report_data))

        # Processing metadata
        if self.template.include_metadata:
            story.extend(self._build_metadata_section(report_data))

        # Clinical notes and recommendations
        if self.template.include_clinical_notes and (
            report_data.physician_notes or report_data.recommendations
        ):
            story.extend(self._build_clinical_notes(report_data))

        # Custom sections
        for section_title, section_content in self.template.custom_sections.items():
            story.append(Paragraph(section_title, self.styles["SectionHeader"]))
            story.append(Paragraph(section_content, self.styles["Normal"]))
            story.append(Spacer(1, 0.3 * inch))

        # Footer
        if self.template.include_disclaimer:
            story.extend(self._build_footer(report_data))

        # Build PDF
        doc.build(story)

        logger.info(f"Generated PDF report: {output_path}")
        return output_path

    def _build_header(self, report_data: StreamingReportData) -> List:
        """Build report header with institution branding."""
        elements = []

        # Institution logo if provided
        if self.institution_logo and self.institution_logo.exists():
            try:
                logo = RLImage(str(self.institution_logo), width=2 * inch, height=0.75 * inch)
                elements.append(logo)
                elements.append(Spacer(1, 0.2 * inch))
            except Exception as e:
                logger.warning(f"Failed to load institution logo: {e}")

        # Title
        title = Paragraph(
            f"<b>{self.institution_name}</b><br/>{self.t('report_title')}",
            self.styles["CustomTitle"],
        )
        elements.append(title)
        elements.append(Spacer(1, 0.3 * inch))

        return elements

    def _build_patient_info(self, report_data: StreamingReportData) -> List:
        """Build patient/slide information section."""
        elements = []

        elements.append(Paragraph(self.t("slide_information"), self.styles["SectionHeader"]))

        # Create info table
        data = [
            [f"{self.t('slide_id')}:", report_data.slide_id],
            [
                f"{self.t('report_date')}:",
                report_data.report_timestamp.strftime("%Y-%m-%d %H:%M:%S"),
            ],
        ]

        if report_data.patient_id:
            data.insert(1, [f"{self.t('patient_id')}:", report_data.patient_id])

        if report_data.scan_date:
            data.append([f"{self.t('scan_date')}:", report_data.scan_date.strftime("%Y-%m-%d")])

        table = Table(data, colWidths=[2 * inch, 4 * inch])
        table.setStyle(
            TableStyle(
                [
                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                    ("FONTSIZE", (0, 0), (-1, -1), 10),
                    ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#555555")),
                    ("ALIGN", (0, 0), (0, -1), "RIGHT"),
                    ("ALIGN", (1, 0), (1, -1), "LEFT"),
                    ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                    ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                ]
            )
        )

        elements.append(table)
        elements.append(Spacer(1, 0.3 * inch))

        return elements

    def _build_results_section(self, report_data: StreamingReportData) -> List:
        """Build primary results section with confidence."""
        elements = []

        elements.append(Paragraph(self.t("analysis_results"), self.styles["SectionHeader"]))

        # Prediction
        pred_text = f"<b>{self.t('prediction')}:</b> {report_data.prediction_class}"
        elements.append(Paragraph(pred_text, self.styles["Metric"]))
        elements.append(Spacer(1, 0.1 * inch))

        # Confidence with color coding
        confidence_pct = report_data.confidence * 100
        if report_data.confidence >= 0.90:
            style = "ConfidenceHigh"
            interpretation = self.t("confidence_high")
        elif report_data.confidence >= 0.70:
            style = "ConfidenceMedium"
            interpretation = self.t("confidence_medium")
        else:
            style = "ConfidenceLow"
            interpretation = self.t("confidence_low")

        conf_text = f"<b>{self.t('confidence')}:</b> {confidence_pct:.1f}%"
        elements.append(Paragraph(conf_text, self.styles[style]))
        elements.append(Paragraph(f"<i>{interpretation}</i>", self.styles["Normal"]))
        elements.append(Spacer(1, 0.1 * inch))

        # Uncertainty quantification if available
        if report_data.uncertainty_score is not None:
            unc_text = f"<b>{self.t('uncertainty_score')}:</b> {report_data.uncertainty_score:.3f}"
            elements.append(Paragraph(unc_text, self.styles["Metric"]))

        if report_data.confidence_interval:
            ci_low, ci_high = report_data.confidence_interval
            ci_text = f"<b>{self.t('confidence_interval')}:</b> [{ci_low:.3f}, {ci_high:.3f}]"
            elements.append(Paragraph(ci_text, self.styles["Metric"]))

        elements.append(Spacer(1, 0.3 * inch))

        return elements

    def _build_visualizations_section(self, report_data: StreamingReportData) -> List:
        """Build visualizations section with attention heatmap and confidence plot."""
        elements = []

        elements.append(Paragraph("Visualizations", self.styles["SectionHeader"]))

        viz_elements = []

        # Attention heatmap
        if report_data.attention_heatmap_path and report_data.attention_heatmap_path.exists():
            try:
                heatmap = RLImage(
                    str(report_data.attention_heatmap_path), width=5 * inch, height=4 * inch
                )
                viz_elements.append(heatmap)
                viz_elements.append(
                    Paragraph(
                        "<i>Figure 1: Attention heatmap showing regions of diagnostic importance</i>",
                        self.styles["Normal"],
                    )
                )
                viz_elements.append(Spacer(1, 0.2 * inch))
            except Exception as e:
                logger.warning(f"Failed to load attention heatmap: {e}")

        # Confidence progression plot
        if report_data.confidence_plot_path and report_data.confidence_plot_path.exists():
            try:
                conf_plot = RLImage(
                    str(report_data.confidence_plot_path), width=5 * inch, height=3 * inch
                )
                viz_elements.append(conf_plot)
                viz_elements.append(
                    Paragraph(
                        "<i>Figure 2: Confidence progression during real-time analysis</i>",
                        self.styles["Normal"],
                    )
                )
            except Exception as e:
                logger.warning(f"Failed to load confidence plot: {e}")

        if viz_elements:
            elements.extend(viz_elements)
        else:
            elements.append(Paragraph("<i>No visualizations available</i>", self.styles["Normal"]))

        elements.append(Spacer(1, 0.3 * inch))

        return elements

    def _build_quality_metrics(self, report_data: StreamingReportData) -> List:
        """Build quality metrics section."""
        elements = []

        elements.append(Paragraph("Quality Metrics", self.styles["SectionHeader"]))

        # Create metrics table
        data = [
            ["Coverage:", f"{report_data.coverage_percent:.1f}%"],
            [
                "Patches Processed:",
                f"{report_data.patches_processed:,} / {report_data.total_patches:,}",
            ],
            ["Processing Time:", f"{report_data.processing_time:.1f}s"],
            ["Throughput:", f"{report_data.throughput:.1f} patches/sec"],
        ]

        if report_data.memory_usage_gb > 0:
            data.append(["Peak Memory Usage:", f"{report_data.memory_usage_gb:.2f} GB"])

        table = Table(data, colWidths=[2.5 * inch, 2.5 * inch])
        table.setStyle(
            TableStyle(
                [
                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                    ("FONTSIZE", (0, 0), (-1, -1), 10),
                    ("TEXTCOLOR", (0, 0), (0, -1), colors.HexColor("#555555")),
                    ("ALIGN", (0, 0), (0, -1), "RIGHT"),
                    ("ALIGN", (1, 0), (1, -1), "LEFT"),
                    ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                    ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                    ("GRID", (0, 0), (-1, -1), 0.5, colors.grey),
                ]
            )
        )

        elements.append(table)
        elements.append(Spacer(1, 0.3 * inch))

        return elements

    def _build_metadata_section(self, report_data: StreamingReportData) -> List:
        """Build processing metadata section."""
        elements = []

        elements.append(Paragraph("Processing Metadata", self.styles["SectionHeader"]))

        data = [
            ["Model Version:", report_data.model_version],
            ["Processing Mode:", report_data.processing_mode.capitalize()],
            ["Analysis Timestamp:", report_data.report_timestamp.strftime("%Y-%m-%d %H:%M:%S UTC")],
        ]

        table = Table(data, colWidths=[2.5 * inch, 3.5 * inch])
        table.setStyle(
            TableStyle(
                [
                    ("FONTNAME", (0, 0), (0, -1), "Helvetica-Bold"),
                    ("FONTSIZE", (0, 0), (-1, -1), 9),
                    ("TEXTCOLOR", (0, 0), (-1, -1), colors.HexColor("#666666")),
                    ("ALIGN", (0, 0), (0, -1), "RIGHT"),
                    ("ALIGN", (1, 0), (1, -1), "LEFT"),
                    ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
                    ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
                ]
            )
        )

        elements.append(table)
        elements.append(Spacer(1, 0.3 * inch))

