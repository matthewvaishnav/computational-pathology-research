@dataclass
class ReportTemplate:
    """Configuration for report template customization."""

    # Layout
    page_size: str = "letter"  # 'letter', 'a4'
    margins: Dict[str, float] = field(
        default_factory=lambda: {"top": 0.75, "bottom": 0.75, "left": 0.75, "right": 0.75}
    )  # in inches

    # Typography
    title_font_size: int = 24
    section_font_size: int = 14
    body_font_size: int = 11

    # Colors (hex)
    primary_color: str = "#2c5aa0"
    confidence_high_color: str = "#2e7d32"
    confidence_medium_color: str = "#f57c00"
    confidence_low_color: str = "#c62828"

    # Sections to include
    include_patient_info: bool = True
    include_visualizations: bool = True
    include_quality_metrics: bool = True
    include_metadata: bool = True
    include_clinical_notes: bool = True
    include_disclaimer: bool = True

    # Custom sections (title: content)
    custom_sections: Dict[str, str] = field(default_factory=dict)


